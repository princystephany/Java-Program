package com.practice;

public class PosOrNeg {
	
	public static void main(String[] args) {
		
		int n= 0;
		
		if(n>=1) {
			System.out.println("the number is positive");
		}
		
		else if(n<=-1) {
			System.out.println("the number is negative");
		}
		
		else {
			System.out.println("the number is neutral");
		}
	}

}
